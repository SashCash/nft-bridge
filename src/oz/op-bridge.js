// Entrypoint for the Autotask
exports.handler = async function (payload) {
  // Initialize defender relayer provider and signer
  const relayProvider = new DefenderRelayProvider(payload);
  const signer = new DefenderRelaySigner(payload, relayProvider, {
    speed: "fast",
  });
  // Call the withdraw function on Optimism, this action is connected to Arbitrum Monitor
  const addressBridge = "0x8100e69Ae3b94370ee16C1D167b793446B608499";
  const provider = new ethers.providers.WebSocketProvider(
    process.env.OP_PROVIDER
  );

  const conditionRequest = payload.request.body;
  console.log(
    "conditionRequest.matchReasons is : ",
    conditionRequest.matchReasons
  );
  for (const evt of conditionRequest.matchReasons) {
    // add custom logic for matching here
    const fromAddress = evt.params.from;
    const tokenId = evt.params.tokenId;
    console.log("fromAddress is : ", fromAddress);
    console.log("tokenId is : ", tokenId);
    const nftContract = new ethers.Contract(addressBridge, fullAbi, signer);
    let tx = await nftContract.withdraw(fromAddress, tokenId);
    await tx.wait();
    console.log("txHash is: ", tx.hash);
  }
  return;
};
